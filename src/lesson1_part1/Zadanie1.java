package lesson1_part1;

public class Zadanie1 {
    public static void main(String[] args) {
        // napisz program, który utworzy dwie zmienne, a następnie wypisze na ekran ich sumę, różnicę i iloczyn
        int x = 10;
        int y = 20;
        int z = x + y;
        int a = x - y;
        int b = x * y;
        System.out.println("Suma wynosi: " + z);
        System.out.println("Różnica wynosi: " + a);
        System.out.println("Iloczyn wynosi: " + b);
    }
}
